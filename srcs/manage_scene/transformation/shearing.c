#include "matrix.h"

t_matrix4_ptr	shearing(double x[2], double y[2], double z[2], t_matrix4 m)
{
	set_identity_matrix(m);
	m[0][1] = x[0];
	m[0][2] = x[1];
	m[1][0] = y[0];
	m[1][2] = y[1];
	m[2][0] = z[0];
	m[2][1] = z[1];
	return (m);
}
